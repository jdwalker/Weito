package featurealgorithm;

public class DrtXlsLocationPair {
	String drtloc;
	String xlsloc;
	/**
	 * @param drtloc
	 * @param xlsloc
	 */
	public DrtXlsLocationPair(String drtloc, String xlsloc) {
		this.drtloc = drtloc;
		this.xlsloc = xlsloc;
	}
	/**
	 * @return the drtloc
	 */
	public String getDrtloc() {
		return drtloc;
	}
	/**
	 * @return the xlsloc
	 */
	public String getXlsloc() {
		return xlsloc;
	}
}
